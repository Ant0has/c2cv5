export function generateCityFaq(cityName: string, cityGenitive: string, minPrice: number, totalCount: number) {
  const priceStr = minPrice > 0 ? minPrice.toLocaleString('ru-RU') : '3 000'

  return [
    {
      question: `Как заказать такси межгород ${cityGenitive}?`,
      answer: `Позвоните по номеру +7 (938) 156-87-57 или оставьте заявку на сайте. Мы подтвердим заказ в течение 5 минут и назначим водителя.`,
    },
    {
      question: `Сколько стоит междугороднее такси ${cityGenitive}?`,
      answer: `Цены на такси межгород ${cityGenitive} начинаются от ${priceStr}₽. Точная стоимость зависит от расстояния, класса автомобиля и времени поездки. Доступно ${totalCount} направлений.`,
    },
    {
      question: `Какие автомобили доступны для поездок ${cityGenitive}?`,
      answer: `Для междугородних поездок ${cityGenitive} доступны автомобили классов Комфорт, Комфорт+, Бизнес и Минивэн. Все автомобили проходят регулярное техническое обслуживание.`,
    },
    {
      question: `Как происходит подача такси в ${cityName}?`,
      answer: `Подача автомобиля в ${cityName} осуществляется по указанному адресу. Среднее время подачи — от 30 минут. Водитель свяжется с вами заранее для уточнения деталей.`,
    },
    {
      question: `Можно ли заказать такси ${cityGenitive} в обе стороны?`,
      answer: `Да, вы можете заказать поездку в обе стороны. При заказе обратного рейса действует скидка 50% на обратный путь.`,
    },
  ]
}
