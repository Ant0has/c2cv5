interface FaqItem {
  question: string
  answer: string
}

const GENERIC_FAQ = (cityName: string, cityGenitive: string, cityLocative: string, totalCount: number, minPrice: number): FaqItem[] => {
  const priceStr = minPrice > 0 ? minPrice.toLocaleString('ru-RU') : '3 000'
  return [
    {
      question: `За сколько подают машину ${cityLocative}?`,
      answer: `В большинстве случаев подаём ${cityLocative} за 30-60 минут с момента подтверждения заказа. Ночью и в выходные время подачи может быть на 15-30 минут больше. На дальние маршруты (от 500 км) чаще заказывают за сутки — водителю нужно доехать до точки старта.`,
    },
    {
      question: `Какие автомобили подают на межгород ${cityGenitive}?`,
      answer: `Четыре класса: Комфорт (Kia Rio, Hyundai Solaris и аналоги), Комфорт+ (Toyota Camry, Kia K5), Бизнес (Mercedes E-Class, BMW 5 Series) и Минивэн до 6-7 пассажиров. Все автомобили иномарки не старше 5 лет, проходят ТО перед каждым рейсом.`,
    },
    {
      question: `Как рассчитывается цена поездки ${cityGenitive}?`,
      answer: `Фиксированная цена по расстоянию и классу автомобиля. На ${totalCount} маршрутов из ${cityName.replace(/^(В|ВО) /, '')} стоимость начинается от ${priceStr}₽. Сумма известна до оформления заказа и не меняется в пути: пробки, объезды и кофе-паузы включены.`,
    },
  ]
}

